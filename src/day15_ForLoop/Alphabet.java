package day15_ForLoop;

public class Alphabet {
    public static void main(String[] args) {

        for ( char a = 'a'; a<='z'; a++)
        {
            System.out.print(a + " ");
        }
        for ( char a = 'Z'; a>='A'; a--)
        {
            System.out.print(a + " ");
        }
        for ( char a = 1; a<=300; a++)
        {
            System.out.print(a + " ");
        }

    }
}
